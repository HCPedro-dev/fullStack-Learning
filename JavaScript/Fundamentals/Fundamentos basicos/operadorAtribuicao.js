const a = 7 // o = neste caso esta igualzando o valor 7 a variavel a 
let b = 3
b += a // aqui estamos somando o valor de a à variavel b (b = b + a)
console.log(b) // imprime o valor de b, que é 10 (7 + 3)


// exemplo de decremento
let c = 10
c -= 3 // aqui estamos subtraindo o valor de 3 da variavel c (c = c - 3)
console.log(c) // imprime o valor de c, que é 7 (10 - 3)


// exemplo de divisão
let d = 15
d /= 5 // aqui estamos dividindo o valor de d por 5 (d = d / 5)
console.log(d) // imprime o valor de d, que é 3 (15 / 5)


// exemplo de multiplicação
let e = 5
e *= 3 // aqui estamos multiplicando o valor de e por 3 (e = e * 3)
console.log(e) // imprime o valor de e, que é 15 (5 * 3)